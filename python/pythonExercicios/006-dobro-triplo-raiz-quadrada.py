#006 Crie um algorítmio que leia um número e mostre o seu dobro, triplo e sua raiz quardada.
print('\n***** NÚMERO EM DOBRO, TRIPLO E RAIZ *****\n')

numero = int(input('Digite um número qualquer: '))
dobro = numero * 2
triplo = numero * 3
raizQuadrada = numero * numero
print(f'DOBRO {dobro} >> TRIPLO {triplo} >> RAÍZ QUADRADA {raizQuadrada}')
